function sayHello(input) {
    if(input === true) {
        return "Hello, World!";
    }else if(input === false) {
        return "Goodbye, World?";
    }else{
        return ("Hello, " + input + "!");
    }
}

/* ---------------------------------------------------------------------------------------------- */

function isFive(number) {
    if(number === 5) {
        return true;
    }else{
        return false;
    }
}

/* ---------------------------------------------------------------------------------------------- */

function isEven(number) {
    return (number % 2 === 0);
}

